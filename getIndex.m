function [i, j] = getIndex(N1,n)
%%%%% get index (i,j) ensuring N1 = (i-1)*(n+1) + j %%%%%%%%%%%%%%%%%%%%%%%
%%%%% for element Q_k %%%
%%% i,j in [1:n]   %%%%%%
%%% |---------|N4-----N3 --
%%% | Q_{k-1} |  Q_k  |  --
%%% |-------- |N1-----N2 --
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

i = fix(N1/(n+1)) + 1;
j = mod(N1,n+1);

end